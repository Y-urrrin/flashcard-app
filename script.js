let currentTheme = [];
let currentIndex = 0;

const themeScreen = document.getElementById("theme-screen");
const cardScreen = document.getElementById("card-screen");

const image = document.getElementById("card-image");
const nameArea = document.getElementById("card-name");

const themeButtons =
  document.querySelectorAll(".theme-btn");

function showCard() {
  const card = currentTheme[currentIndex];

  image.src = card.image;
  image.alt = card.name;

  nameArea.textContent = "";
}

function speak(text) {
  const speech =
    new SpeechSynthesisUtterance(text);

  speech.lang = "ja-JP";

  speechSynthesis.cancel();
  speechSynthesis.speak(speech);
}

themeButtons.forEach(button => {
  button.addEventListener("click", () => {

    const themeName =
      button.dataset.theme;

    currentTheme = [...themes[themeName]];

    // ランダムに並び替え
    currentTheme.sort(() => Math.random() - 0.5);

    currentIndex = 0;

    themeScreen.classList.add("hidden");
    cardScreen.classList.remove("hidden");

    showCard();
  });
});

image.addEventListener("click", () => {
  const card = currentTheme[currentIndex];

  nameArea.textContent = card.name;
  speak(card.name);
});

document
  .getElementById("next-btn")
  .addEventListener("click", () => {

    currentIndex++;

    if (currentIndex >= currentTheme.length) {
      currentIndex = 0;

      currentTheme.sort(
        () => Math.random() - 0.5
      );
    }

    showCard();
  });

document
  .getElementById("back-btn")
  .addEventListener("click", () => {

    cardScreen.classList.add("hidden");
    themeScreen.classList.remove("hidden");
  });

  if ("serviceWorker" in navigator) {
  navigator.serviceWorker
    .register("./service-worker.js")
    .then(() => {
      console.log("Service Worker 登録完了");
    });
}